// Get URL parameters
function getUrlParams() {
  const urlParams = new URLSearchParams(window.location.search);
  return {
    model: urlParams.get('model'),
    name: urlParams.get('name'),
    specs: urlParams.get('specs'),
    image: urlParams.get('image')
  };
}

// Load phone template
function loadPhoneTemplate() {
  const params = getUrlParams();
  const template = document.getElementById('template');
  const phoneNameSpan = document.getElementById('selected-phone-name');
  const phoneSpecsP = document.getElementById('selected-phone-specs');
  
  // Default to iPhone 11 if no model specified
  const phoneModel = params.model || 'iphone-11';
  const phoneData = phoneTemplates[phoneModel] || phoneTemplates['iphone-11'];
  
  // Update template image
  template.src = phoneData.template;
  
  // Update phone info display
  phoneNameSpan.textContent = phoneData.name;
  phoneSpecsP.textContent = phoneData.specs;
  
  // Store phone data for use in script.js
  window.selectedPhoneData = phoneData;
  
  // Add loading animation
  template.style.opacity = '0';
  template.style.transition = 'opacity 0.5s ease-in-out';
  
  template.onload = () => {
    template.style.opacity = '1';
  };
  
  template.onerror = () => {
    console.error('Failed to load template image:', phoneData.template);
    // Fallback to default template
    template.src = phoneTemplates['iphone-11'].template;
  };
}

// Initialize when DOM is loaded
document.addEventListener('DOMContentLoaded', () => {
  loadPhoneTemplate();
  
  // Add smooth transition for phone info display
  const phoneInfoDisplay = document.getElementById('phone-info-display');
  phoneInfoDisplay.style.opacity = '0';
  phoneInfoDisplay.style.transform = 'translateY(20px)';
  phoneInfoDisplay.style.transition = 'all 0.5s ease-in-out';
  
  setTimeout(() => {
    phoneInfoDisplay.style.opacity = '1';
    phoneInfoDisplay.style.transform = 'translateY(0)';
  }, 300);
});

